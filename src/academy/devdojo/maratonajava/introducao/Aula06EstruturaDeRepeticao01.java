package academy.devdojo.maratonajava.introducao;

public class Aula06EstruturaDeRepeticao01 {
    public static void main (String[] args){
        //while, do while, for
        int count = 0;
        while(count < 10){
            System.out.println(++count);

        }

        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }
    }
}
